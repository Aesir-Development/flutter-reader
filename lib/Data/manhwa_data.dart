  };
  
  return idToKeyMap[idStr] ?? idStr;
}

// Helper function to get manhwa by ID (in-memory fallback)
Manhwa? getManhwaById(dynamic manhwaId) {
  String key = getManhwaKey(manhwaId);
  return manhwaDatabase[key];
}

